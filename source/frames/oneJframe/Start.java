    /**
     * Creates new form Start
     */
    public Start() {
        frame = new JFrame();
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frame.setTitle("Escala Test");
        frame.setSize( scaledWidth, scaledHeight);
        frame.setVisible(true);
        
        
        
        addIndexCard(cards);
        addDiffCard(cards);
        
        frame.add(cards);
        
        play.addActionListener(e -> tst(frame));
    
       
        frame.setVisible(true);
    }
    
    public void addIndexCard(Container pane){
        JPanel index = new JPanel();
        index.add(new JLabel("ESCALA"));
        index.add(play);
        index.add(new JButton("SETTINGS"));
        index.add(new JButton("CREDITS"));
        index.add(new JButton("EXIT"));
        
        pane.add(index, "INDEX");
        
    }    
    
    public void addDiffCard(Container pane){
        JPanel diff = new JPanel();
        diff.add(new JLabel("DIFFICULTY"));
        diff.add(new JButton("EASY"));
        diff.add(new JButton("MEDIUM"));
        diff.add(new JButton("HARD"));
        
        pane.add(diff, "DIFF");
    }
    
    
    
    
    
        

